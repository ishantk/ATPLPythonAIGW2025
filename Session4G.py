# Increment Decrement comes under Assignment Operators
# +=, -=, *=, /=, **=, //=, %=

number = 10
number += 1 # number = number + 1 number = 10 + 1 -> number = 11
number *= 2 # number = number * 2 number = 11 * 2 -> number = 22

print('number:', number)


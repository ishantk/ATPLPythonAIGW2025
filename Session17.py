# Empty Data Structures

# Empty Tuple
# my_tuple = ()
my_tuple = tuple()

print('my_tuple:', my_tuple, type(my_tuple))

# Empty List
# my_list = []
my_list = list()
print('my_list:', my_list, type(my_list))

# Empty Set
my_set = set()
print('my_set:', my_set, type(my_set))

# Empty Dictionary
# my_dictionary = {}
my_dictionary = dict()
print('my_dictionary:', my_dictionary, type(my_dictionary))
